A = {2, 4, 6, 8, 10, 12, 14, 16, 18}
B = {3, 5, 7, 9, 11, 13, 15, 17, 19}
C = {-9, -8, -7, -6, -5, -4, -3, -2, -1}
D = {2, 3, 5, 7, 11, 13, 17, 19}
E = {4, 6, 8, 9, 10, 12, 14, 15, 16, 18}

# a. A ∪ B ∪ C ∪ D ∪ E
print(A | B | C | D | E)

# b. (A ∩ B) ∪ (D ∩ E)
print((A & B) | (D & E))

# c. (A ∪ B) ∩ (D ∪ E)
print((A | B) & (D | E))

# d. (A ∪ B) - (D ∪ E)
print((A | B) - (D | E))

# e. (A ∪ B ∪ C) - (A ∩ E)
print((A | B | C) - (A & E))